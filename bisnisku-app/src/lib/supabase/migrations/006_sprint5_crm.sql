CREATE INDEX idx_interactions_type ON customer_interactions(business_id, type);
CREATE INDEX idx_interactions_occurred ON customer_interactions(customer_id, occurred_at DESC);

-- ============================================================
-- FUNCTION: Auto-update customer metrics after interaction
-- ============================================================
CREATE OR REPLACE FUNCTION update_customer_metrics()
RETURNS TRIGGER AS $$
BEGIN
  -- Update visit count and last visit
  IF NEW.type = 'visit' THEN
    UPDATE customers SET
      total_visits = total_visits + 1,
      last_visit_at = GREATEST(last_visit_at, NEW.occurred_at),
      first_visit_at = LEAST(COALESCE(first_visit_at, NEW.occurred_at), NEW.occurred_at)
    WHERE id = NEW.customer_id;
  END IF;

  -- Update spending
  IF NEW.amount IS NOT NULL AND NEW.amount > 0 THEN
    UPDATE customers SET
      total_spent = total_spent + NEW.amount,
      average_spend = (total_spent + NEW.amount) / NULLIF(total_visits, 0)
    WHERE id = NEW.customer_id;
  END IF;

  -- Auto-stage upgrade
  UPDATE customers SET
    stage = CASE
      WHEN total_visits > 10 OR total_spent > 5000000 THEN 'vip'
      WHEN total_visits >= 1 THEN 'customer'
      ELSE stage
    END
  WHERE id = NEW.customer_id
    AND stage NOT IN ('vip', 'churned');

  RETURN NEW;
END;
$$ LANGUAGE plpgsql SECURITY DEFINER;

CREATE TRIGGER trg_update_customer_metrics
  AFTER INSERT ON customer_interactions
  FOR EACH ROW EXECUTE FUNCTION update_customer_metrics();

