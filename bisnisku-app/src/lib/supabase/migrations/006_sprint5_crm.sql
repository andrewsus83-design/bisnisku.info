CREATE TYPE customer_stage AS ENUM (
  'lead', 'prospect', 'customer', 'vip', 'churned'
);

CREATE TYPE interaction_type AS ENUM (
  'visit', 'purchase', 'booking', 'inquiry',
  'wa_message', 'review', 'referral', 'complaint',
  'loyalty_redeem', 'promo_used'
);

CREATE TYPE auto_tag_rule AS ENUM (
  'vip',           -- >10 visits
  'new',           -- first 30 days
  'at_risk',       -- >60 days no visit
  'big_spender',   -- >Rp5M total spend
  'loyal',         -- >6 months active
  'birthday_soon', -- birthday within 7 days
  'inactive'       -- >90 days no visit
);

-- ========================
-- CUSTOMERS
-- Central customer record, one per phone/email per business
-- ========================
CREATE TABLE customers (
  id            UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  business_id   UUID NOT NULL REFERENCES businesses(id) ON DELETE CASCADE,
  name          TEXT NOT NULL,
  phone         TEXT,
  email         TEXT,
  avatar_url    TEXT,
  stage         customer_stage DEFAULT 'lead',
  source        TEXT DEFAULT 'manual',           -- manual, wa, booking, import, walk_in
  notes_summary TEXT,                            -- AI-generated summary of recent notes

  -- Computed/cached metrics (updated by triggers or cron)
  total_visits    INTEGER DEFAULT 0,
  total_spent     NUMERIC(14,2) DEFAULT 0,
  last_visit_at   TIMESTAMPTZ,
  first_visit_at  TIMESTAMPTZ,
  average_spend   NUMERIC(12,2) DEFAULT 0,
  lifetime_points INTEGER DEFAULT 0,

  -- Personal info
  birthday      DATE,
  address       TEXT,
  gender        TEXT,                            -- 'M', 'F', or NULL

  -- Meta
  is_active     BOOLEAN DEFAULT TRUE,
  created_at    TIMESTAMPTZ DEFAULT now(),
  updated_at    TIMESTAMPTZ DEFAULT now(),

  -- One customer per phone per business (if phone provided)
  UNIQUE NULLS NOT DISTINCT (business_id, phone),
  -- One customer per email per business (if email provided)
  UNIQUE NULLS NOT DISTINCT (business_id, email)
);

CREATE INDEX idx_customers_business ON customers(business_id);
CREATE INDEX idx_customers_stage ON customers(business_id, stage);
CREATE INDEX idx_customers_phone ON customers(business_id, phone) WHERE phone IS NOT NULL;
CREATE INDEX idx_customers_name_search ON customers USING gin(name gin_trgm_ops);
CREATE INDEX idx_customers_last_visit ON customers(business_id, last_visit_at DESC NULLS LAST);
CREATE INDEX idx_customers_total_spent ON customers(business_id, total_spent DESC);

CREATE TRIGGER customers_updated_at
  BEFORE UPDATE ON customers
  FOR EACH ROW EXECUTE FUNCTION update_updated_at();

